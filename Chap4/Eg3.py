def show_number():
    for i in range (1,5):
        print(i)
show_number()

def show_number(n):
    for i in range (1,n+1):
        print(i)
show_number(5)

def show_number(m,n):
    for i in range (m,n+1):
        print(i)
show_number(1,5)
show_number(5,10)